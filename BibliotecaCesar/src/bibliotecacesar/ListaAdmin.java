    private void jBexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBexcluirActionPerformed
        String cod = jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString();
        
        String query = "DELETE FROM `biblioteca_cesar`.`livro` WHERE `cod` = '"+cod+"';";
        
        try {
                Statement stmt = ConexaoBD.conecta().createStatement();
                stmt.execute(query);
                    JOptionPane.showMessageDialog(this, "Dados excluídos com sucesso!");
                stmt.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erro: "+e.getMessage());
            }
        ((DefaultTableModel) jTable2.getModel()).setRowCount(0);
        alimentatabela();
    }//GEN-LAST:event_jBexcluirActionPerformed

