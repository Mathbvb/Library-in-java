    public void alimentatabela(){
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
               
        String query = "SELECT * FROM `biblioteca_cesar`.`livro`";
        
        try {
            Statement stmt = ConexaoBD.conecta().createStatement();
            ResultSet result = stmt.executeQuery(query);
            
            while (result.next()){
                modelo.addRow(
                        new Object[]{
                            result.getString("cod"),
                            result.getString("titulo"),
                            result.getString("autor"),
                            result.getString("edicao"),
                            result.getString("publicacao"),
                            result.getString("reservado")
                        }
                );
            }
            stmt.close();
        } catch (Exception e) {
        }
    }

